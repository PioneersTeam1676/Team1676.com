							                                <li> Take safety certification exams to be qualified to use mechanical tools. </li>
							                                <li> Sign safety contracts to ensure they are following cautious procedures. </li>
							                                <li> Maintain the first aid kits. </li>
							                                <li> Participate in safety updates weekly. </li>
							                                <li> Review safety violations and discuss ways to improve. </li>
							                                <li> Stay organized at competitions to ensure everyone is safe during events - in the stands </li>
